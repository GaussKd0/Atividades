public class LerNumeros {

	public static void main(String[] args) {
		
		int x = 7;
		int y = 0;
		
		if(x == y) {
			
			System.out.println("Os Inteiros São Iguais\n");
		}
		
		if(x != y) {
			
			System.out.println("Os Inteiros São Diferentes\n");
		}
		
		if (x > y) {
			
			System.out.println("X é maior que Y\n");
		}
		
		if (x < y) {
			
			System.out.println("X é menor que Y\n");
		}
		
        if (x <= y) {
			
			System.out.println("X é menor ou igual a Y\n");
		}
        
        
        if (x >= y) {
			
			System.out.println("X é maior ou igual a Y\n");
		}
		
	}
}
